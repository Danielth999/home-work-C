#include <stdio.h>

int main()
{
    float base, height, area; // ประกาศตัวแปร ฐาน สูง และพื้นที่ เป็น float

    printf("กรุณาใส่ความยาวฐานของสามเหลี่ยม: "); // ขอให้ผู้ใช้ป้อนค่าฐาน
    scanf("%f", &base);                      // รับค่าฐานจากผู้ใช้

    printf("กรุณาใส่ความสูงของสามเหลี่ยม: "); // ขอให้ผู้ใช้ป้อนค่าสูง
    scanf("%f", &height);                // รับค่าสูงจากผู้ใช้

    area = 0.5 * base * height; // คำนวณพื้นที่สามเหลี่ยม โดยใช้สูตร 1/2 * ฐาน * สูง

    printf("พื้นที่ของสามเหลี่ยมคือ: %.2f ตารางหน่วย\n", area); // แสดงผลลัพธ์จากตัวแปร area

    return 0; // จบการทำงานของโปรแกรม
}