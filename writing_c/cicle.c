#include <stdio.h>

int main() {
    float radius, area; // ประกาศตัวแปรรัศมีและพื้นที่

    printf("กรุณาใส่รัศมีของวงกลม: "); // ขอให้ผู้ใช้ป้อนค่ารัศมี
    scanf("%f", &radius); // รับค่ารัศมีจากผู้ใช้

    area = 3.14159 * radius * radius; // คำนวณพื้นที่วงกลมด้วยสูตร πr² หรือก็คือ 3.14159 * r * r

    printf("พื้นที่ของวงกลมคือ: %.2f ตารางหน่วย\n", area); // แสดงผลลัพธ์โดยใช้ทศนิยม 2 ตำแหน่ง

    return 0; // จบการทำงานของโปรแกรม
}