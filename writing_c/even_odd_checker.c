#include <stdio.h>

int main() {
    int num; // ประกาศตัวแปรสำหรับเก็บค่าตัวเลข

    printf("กรุณาใส่ตัวเลขจำนวนเต็ม: "); // ขอให้ผู้ใช้ป้อนค่าตัวเลข
    scanf("%d", &num); // รับค่าตัวเลขจากผู้ใช้

    if(num % 2 == 0) { // ตรวจสอบว่าเป็นเลขคู่มั้ย
        printf("%d เป็นเลขคู่ \n", num); // แสดงผลว่าเป็นเลขคู่
    } else {
        printf("%d เป็นเลขคี่ \n", num); // แสดงผลว่าเป็นเลขคี่
    }

    return 0; // จบการทำงานของโปรแกรม
}
