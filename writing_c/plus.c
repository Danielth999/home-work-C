#include <stdio.h> // เรียกใช้ไลบรารีสำหรับฟังก์ชัน printf และ scanf

int main() { // ฟังก์ชันหลักของโปรแกรม
    int num1, num2, sum; // ประกาศตัวแปรสำหรับเก็บค่าตัวเลขและผลรวม

    printf("กรุณาใส่ตัวเลขตัวที่ 1: "); // ขอให้ผู้ใช้ป้อนค่าตัวเลขตัวที่ 1
    scanf("%d", &num1); // รับค่าตัวเลขตัวที่ 1 จากผู้ใช้ เป็นจำนวนเต็ม %d

    printf("กรุณาใส่ตัวเลขตัวที่ 2: "); // ขอให้ผู้ใช้ป้อนค่าตัวเลขตัวที่ 2
    scanf("%d", &num2); // รับค่าตัวเลขตัวที่ 2 จากผู้ใช้ เป็นจำนวนเต็ม %d

    sum = num1 + num2; // คำนวณผลรวมของตัวเลขที่ผู้ใช้ป้อนเข้ามา

    printf("ผลรวมคือ: %d\n", sum); // แสดงผลลัพธ์

    return 0; // จบการทำงานของโปรแกรม
}
